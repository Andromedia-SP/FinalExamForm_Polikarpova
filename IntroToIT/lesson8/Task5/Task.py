# Задача: сгенерировать список всех четных чисел до N
def generate_evens(n):
    """
    Функция для создания списка четных чисел до определенного числа
    :param n: число, до которого нужно генерировать четные числа
    :return : список с четными числами до числа n (включая его, если оно четное)
    """
    return [i for i in range(2, n + 1, 2)]
