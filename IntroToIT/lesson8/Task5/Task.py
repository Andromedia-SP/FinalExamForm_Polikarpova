# Задача: сгенерировать список всех четных чисел до N
def generate_evens(n):
    '''
    Функция для генерации списка, состояещего только из четных чисел \
        до заданного числа
    :param n: Число
    :return: Список чисел
    '''
    return [i for i in range(n+1) if i % 2 == 0]  
